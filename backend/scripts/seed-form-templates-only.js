// Corregir DATABASE_URL: codificar caracteres especiales en la contraseña
function fixDatabaseUrl(url) {
  if (!url || !url.startsWith('postgresql://')) return url;
  const match = url.match(/^(postgresql:\/\/[^:]+:)([^@]+)(@.+)$/);
  if (!match) return url;
  const password = match[2]
    .replace(/#/g, '%23')
    .replace(/\?/g, '%3F')
    .replace(/@/g, '%40')
    .replace(/:/g, '%3A')
    .replace(/\//g, '%2F');
  process.env.DATABASE_URL = match[1] + password + match[3];
}

if (process.env.DATABASE_URL) {
  fixDatabaseUrl(process.env.DATABASE_URL);
}

