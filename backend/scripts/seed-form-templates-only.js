async function seedFormTemplates() {
  try {
    console.log('🚀 Iniciando creación de plantillas de formulario de especialidad...');

    // Limpiar plantillas existentes
    console.log('🧹 Limpiando plantillas existentes...');
    try {
      await prisma.templateField.deleteMany();
      console.log('✅ Campos de plantilla eliminados');
    } catch (e) {
      console.log('⚠️ No hay campos de plantilla para eliminar');
    }
    
    try {
      await prisma.formTemplate.deleteMany();
      console.log('✅ Plantillas eliminadas');
    } catch (e) {
      console.log('⚠️ No hay plantillas para eliminar');
    }

    console.log('✅ Plantillas limpiadas exitosamente');

    // Crear plantillas de especialidad
    console.log('🌱 Creando plantillas de especialidad...');
    // El orden lógico se maneja en el controlador, aquí mantenemos el orden alfabético para el seed
    especialidades.sort((a, b) => a.name.localeCompare(b.name, 'es'));
    
    for (const esp of especialidades) {
      const template = await prisma.formTemplate.create({
        data: {
          name: esp.name,
          specialty: esp.specialty,
          description: `Formulario para ${esp.name.toLowerCase()}`,
          fields: {
            create: esp.fields.map((f, idx) => ({
              label: f.label,
              fieldType: f.fieldType,
              placeholder: f.placeholder || '',
              options: f.options || [],
              isRequired: f.isRequired,
              order: idx + 1
            }))
          }
        }
      });
      console.log(`✅ Creada: ${template.name}`);
    }

    console.log('\n🎉 ¡Plantillas de especialidad creadas exitosamente!');
    console.log(`📊 Total de plantillas creadas: ${especialidades.length}`);
    console.log('\n📋 Plantillas disponibles:');
    especialidades.forEach((esp, index) => {
      console.log(`   ${index + 1}. ${esp.name}`);
    });

  } catch (error) {
    console.error('❌ Error al crear plantillas:', error);
  } finally {
    await prisma.$disconnect();
  }
}

// Ejecutar el script
seedFormTemplates();
